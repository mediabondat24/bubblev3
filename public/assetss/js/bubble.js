// Fungsi mendapatkan TOP 3 pemberi hadiah berdasarkan jumlah diamond
function getTopGivers() {
    return Object.entries(giftGivers)
        .sort(([, a], [, b]) => b.total_diamonds - a.total_diamonds)
        .slice(0, 3) // Ambil top 3
        .map(([uniqueId, data], index) => ({
            rank: `${index + 1}`,
            nickname: data.nickname,
            profilePictureUrl: data.profilePictureUrl,
            diamondCount: data.total_diamonds
        }));
}

// Fungsi memperbarui tampilan setelah ada pemberi hadiah
function updateTopGivers() {
    const spenderContainer = document.querySelector('.spender-container');

    // Ambil data top 3 pemberi hadiah
    const topGivers = getTopGivers();

    // Hapus elemen lama (TETAPI TIDAK menghapus gambar maskot crownImg)
    document.querySelectorAll('.text1, .text2, .text3, .text1-point, .text2-point, .text3-point, .gambar1, .gambar2, .gambar3').forEach(el => el.remove());

    // Posisi untuk teks dan gambar
    const positions = [
        { top: 135, left: 50 },
        { top: 295, left: 25 },
        { top: 295, left: 75 }
    ];

    // Tambahkan elemen baru berdasarkan pemberi hadiah
    for (let index = 0; index < 3; index++) {
        const giver = topGivers[index] || {}; // Jika belum ada pemberi hadiah, gunakan default
        const { nickname, profilePictureUrl, diamondCount } = giver;
        const { top, left } = positions[index];

        // Nama pemberi hadiah (atau default)
        const textElement = document.createElement('div');
        textElement.textContent = nickname || `Nama TOP ${index + 1}`;
        textElement.classList.add(`text${index + 1}`);
        textElement.style.top = `${top}px`;
        textElement.style.left = `${left}%`;
        textElement.style.transform = 'translateX(-50%)';
        spenderContainer.appendChild(textElement);

        // POIN : total diamond (default 0 jika tidak ada pemberi hadiah)
        const pointElement = document.createElement('div');
        pointElement.textContent = `POIN : ${diamondCount || 0}`;
        pointElement.classList.add(`text${index + 1}-point`);
        pointElement.style.top = `${top + 25}px`;
        pointElement.style.left = `${left}%`;
        pointElement.style.transform = 'translateX(-50%)';
        spenderContainer.appendChild(pointElement);

        // Gambar profil pemberi hadiah (akan menggantikan gambar default)
        const imgElement = document.createElement('img');
        imgElement.src = profilePictureUrl || `images/bubble${index + 1}.gif`;
        imgElement.classList.add(`gambar${index + 1}`);
        imgElement.style.position = 'absolute';
        imgElement.style.borderRadius = '50%';
        spenderContainer.appendChild(imgElement);
    }
}

// Fungsi animasi flip pada gambar di spender-container
function addFlipAnimation() {
    const images = document.querySelectorAll('.spender-container img:not(.crownImg1):not(.crownImg2):not(.crownImg3)'); // Jangan animasikan maskot
    images.forEach((img) => {
        img.classList.remove('flip-right', 'flip-left');
        const randomDirection = Math.random() > 0.5 ? 'flip-right' : 'flip-left';
        img.classList.add(randomDirection);
    });
}

